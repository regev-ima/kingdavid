-- Catalog price precision fix — exact incl-VAT per the 2026 price list.
--
-- The catalog prices in the Excel master ("מחירון ... 2026") are the FINAL
-- customer prices, INCLUDING VAT (18%). The app stores product_variations.final_price
-- PRE-VAT (the product page shows it under "לפני מע״מ" and derives incl-VAT as ×1.18;
-- quotes/orders do the same). Earlier the pre-VAT value had been rounded to a whole
-- shekel, so reconstructing incl-VAT (×1.18) drifted by ~₪1 (e.g. QUB80190 showed
-- ₪2,836 instead of the catalog ₪2,835).
--
-- This migration sets final_price = round(inclVatPrice / 1.18, 2) for every SKU in
-- the price list, so that Math.round(final_price * 1.18) reproduces the catalog
-- incl-VAT price EXACTLY, everywhere (product page, quote, order, PDF).
--
-- Match is on the SKU with an optional -S/-D suffix (the importer appends -S for
-- single / -D for double) stripped, so it works whether or not the suffix is present.
-- Idempotent: it SETs absolute values; SKUs not present are untouched (no-op).
